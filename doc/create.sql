---创建用户表
CREATE TABLE SYS_USER
(
  USER_ID VARCHAR2(64 BYTE) NOT NULL
, USER_NAME VARCHAR2(255 BYTE)
, PASSWORD VARCHAR2(50 BYTE)
, LOGIN_NAME VARCHAR2(50 BYTE)
, EMAIL VARCHAR2(50 BYTE)
, PHONE VARCHAR2(20 BYTE)
, EXPIRED_DATE DATE
, CREATE_DATE DATE
, UPDATE_DATE DATE
, DEL_FLAG CHAR(1 BYTE)
, REMARKS VARCHAR2(1024 BYTE)
, CONSTRAINT SYS_USER_PK PRIMARY KEY
  (
    USER_ID
  )
) ;


CREATE TABLE SYS_MENU
(
  MENU_ID VARCHAR2(20 BYTE) NOT NULL
, PARENT_ID VARCHAR2(20 BYTE)
, MENU_NAME VARCHAR2(100 BYTE)
, MENU_URL VARCHAR2(100 BYTE)
, MENU_TYPE VARCHAR2(20 BYTE)
, IS_SHOW NUMBER(*, 0)
, MENU_SORT VARCHAR2(20 BYTE)
, CONSTRAINT SYS_MENU_PK PRIMARY KEY
  (
    MENU_ID
  )
  USING INDEX
  (
      CREATE UNIQUE INDEX SYS_MENU_PK ON SYS_MENU (MENU_ID ASC)
  )
) ;

COMMENT ON COLUMN SYS_MENU.MENU_ID IS '菜单唯一标识';

COMMENT ON COLUMN SYS_MENU.PARENT_ID IS '父级编号';

COMMENT ON COLUMN SYS_MENU.MENU_NAME IS '菜单名称';

COMMENT ON COLUMN SYS_MENU.MENU_URL IS '菜单Url';

COMMENT ON COLUMN SYS_MENU.MENU_TYPE IS '菜单类型';

COMMENT ON COLUMN SYS_MENU.IS_SHOW IS '是否显示 0：隐藏 1：显示';

COMMENT ON COLUMN SYS_MENU.MENU_SORT IS '菜单排序';

