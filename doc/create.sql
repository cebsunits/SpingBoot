----角色用户关联表
CREATE TABLE SYS_USER_ROLE
(
  USER_ID VARCHAR2(64 BYTE) NOT NULL
, ROLE_ID VARCHAR2(64 BYTE) NOT NULL
, CONSTRAINT SYS_USER_ROLE_PK PRIMARY KEY
  (
    USER_ID
  , ROLE_ID
  )
  USING INDEX
  (
      CREATE UNIQUE INDEX SYS_USER_ROLE_PK ON SYS_USER_ROLE (USER_ID ASC, ROLE_ID ASC)
  )
) ;

COMMENT ON COLUMN SYS_USER_ROLE.USER_ID IS '用户ID';

COMMENT ON COLUMN SYS_USER_ROLE.ROLE_ID IS '角色ID';
----系统日志表
CREATE TABLE SYS_LOG
(
  LOG_ID VARCHAR2(64 BYTE) NOT NULL
, USER_NAME VARCHAR2(50 BYTE)
, HOST VARCHAR2(20 BYTE)
, ACTION VARCHAR2(255 BYTE)
, EVENT VARCHAR2(1024 BYTE)
, INSERT_TIME TIMESTAMP(6)
, CONSTRAINT SYS_LOG_PK PRIMARY KEY
  (
    LOG_ID
  )
);

COMMENT ON COLUMN SYS_LOG.LOG_ID IS '日志主键';

COMMENT ON COLUMN SYS_LOG.USER_NAME IS '用户名';

COMMENT ON COLUMN SYS_LOG.HOST IS '访问主机';

COMMENT ON COLUMN SYS_LOG.ACTION IS '行为';

COMMENT ON COLUMN SYS_LOG.EVENT IS '事件描述';

COMMENT ON COLUMN SYS_LOG.INSERT_TIME IS '操作时间';
